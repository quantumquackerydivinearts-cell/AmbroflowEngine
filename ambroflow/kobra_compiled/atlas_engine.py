class AtlasCacheDecl(AtlasDecl):
    """AtlasCacheDecl — hot LRU page cache (Sael = leftovers/cache)."""
    max_pages: int  = 64
    pages:     dict = dataclasses.field(default_factory=dict)
    tongue_eq: str  = "MavoSaelWuUng"

    def is_hot(self, path: str) -> bool:
        return path in self.pages

    def promote(self, path: str, data: bytes) -> None:
        if path not in self.pages and len(self.pages) >= self.max_pages:
            evict_key = next(iter(self.pages))
            del self.pages[evict_key]
        self.pages[path] = data

    def evict(self, path: str) -> None:
        self.pages.pop(path, None)

