def atlas_render_eval(cluster:  "AtlasClusterDecl",
                      cache:    "AtlasCacheDecl",
                      snapshot: "AtlasSnapshotDecl") -> dict:
    """AtlasRenderEval — full atlas pipeline.

    Ouranos  — virtual address table (vaddr per tile)
    Oceanus  — RAM backing bytearray
    Mnemosyne— hot LRU page cache
    Gyges    — DMA transfer descriptors (wide bus)
    Theia    — GPU atlas sheet descriptor (output)
    """
    all_files = [f for d in cluster.dirs for f in d.files]
    if not all_files:
        return {"width": 0, "height": 0, "data": b"", "vaddrs": {}}

    tile_w = max((f.width  for f in all_files), default=0)
    tile_h = max((f.height for f in all_files), default=0)
    if tile_w == 0 or tile_h == 0:
        return {"width": 0, "height": 0, "data": b"", "vaddrs": {}}

    cols    = max(1, int(len(all_files) ** 0.5))
    rows    = (len(all_files) + cols - 1) // cols
    sheet_w = cols * tile_w
    sheet_h = rows * tile_h
    sheet   = bytearray(sheet_w * sheet_h * 4)
    vaddrs: dict = {}

    for idx, tf in enumerate(all_files):
        sx   = (idx % cols) * tile_w
        sy   = (idx // cols) * tile_h
        data = cache.pages.get(tf.path) or atlas_page_el(tf)
        tile_row_bytes = tile_w * 4
        for row in range(tile_h):
            src_off = row * tile_row_bytes
            dst_off = ((sy + row) * sheet_w + sx) * 4
            chunk   = data[src_off:src_off + tile_row_bytes]
            sheet[dst_off:dst_off + len(chunk)] = chunk
        vaddrs[tf.path] = AtlasVAddrDecl(
            base_offset=idx, sheet_x=sx, sheet_y=sy, tile_w=tile_w, tile_h=tile_h)

    return {
        "width":  sheet_w,
        "height": sheet_h,
        "data":   bytes(sheet),
        "vaddrs": vaddrs,
    }

