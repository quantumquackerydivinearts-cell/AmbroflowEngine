def julia_fa_ung(z0: complex, c: complex, max_iter: int = 512) -> float:
    """JuliaFaUng — smooth Julia-set iteration count for one starting point."""
    import math
    z = z0
    for i in range(max_iter):
        if abs(z) > 2.0:
            log_zn = math.log(abs(z))
            nu     = math.log(log_zn / math.log(2)) / math.log(2)
            return float(i) + 1.0 - nu
        z = z * z + c
    return float(max_iter)

