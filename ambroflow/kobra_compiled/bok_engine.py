# ── AzothLo — base accumulation loop ─────────────────────────────────────────
# Kobra: [MavoAzothLo MavoSamos MavoEp MavoSy MavoKael MavoGaohNaAERuKi MavoWuUng]
# Maps to: _derive_azoth() in ambroflow.ko.breath

def azoth_lo(
    layer_densities: dict[int, float],
    coil_position:   float,
    flag_weight:     float = 0.0,
) -> complex:
    """AzothLo — derives the Azoth complex number from 24 layer densities.

    Lotus layers (1–8):  material ground → real axis
    Rose layers (9–16):  relational state → imaginary axis
    Sakura layers (17–24): orientation scale factor applied to both axes
    Flag weight: adds a bounded spiral contribution.
    """
    lotus_vals  = [layer_densities.get(i, 0.5) for i in range(1,  9)]
    rose_vals   = [layer_densities.get(i, 0.5) for i in range(9,  17)]
    sakura_vals = [layer_densities.get(i, 0.5) for i in range(17, 25)]

    lotus_mean  = statistics.mean(lotus_vals)
    rose_mean   = statistics.mean(rose_vals)
    sakura_mean = statistics.mean(sakura_vals)

    real   = lotus_mean  * sakura_mean * 4.0 - 2.0
    imag   = rose_mean   * sakura_mean * 4.0 - 2.0
    spiral = flag_weight * 0.05
    return complex(real + spiral, imag + spiral)


# ── MobiusCoil — Gaoh c-parameter ────────────────────────────────────────────
# Kobra: [MavoMobiusCoil MavoAzothLo MavoAonkiel Ha MavoWuUng MavoSy MavoKael MavoSao]
# Maps to: _gaoh_constant() in ambroflow.ko.breath

def mobius_coil(coil_position: float) -> complex:
    """MobiusCoil — Möbius coil constant: Gaoh as Julia c-parameter.

    Real part:      31/255 ≈ 0.122  (Gaoh = 31, normalized)
    Imaginary part: sin(θ) × 0.1   (encodes coil position as Möbius fold)
    At coil 0 and 12 (the Möbius pair): imaginary = 0.
    """
    real  = 31 / 255.0
    theta = (coil_position / 12.0) * 2 * math.pi
    imag  = math.sin(theta) * 0.1
    return complex(real, imag)


# ── CoilEp — advance the coil ────────────────────────────────────────────────
# Kobra: [MavoCoilEp MavoMobiusCoil MavoGaoh MavoAonkiel Ha MavoLoAo Kyra MavoMekha MavoSao]

def coil_ep(coil_position: float, n_games: int = 31) -> float:
    """CoilEp — advance coil position by one game increment.

    The Möbius coil completes one full rotation across all 31 games.
    """
    return (coil_position + 12.0 / n_games) % 12.0


# ── JuliaFaUng — smooth iteration for one complex point ──────────────────────
# Kobra: [MavoJuliaFaUng MavoShiKaKoKiEl MavoLoAo Kyra ... MavoSava MavoKael]
# Maps to: _mandelbrot_smooth() in ambroflow.ko.render

_MAX_ITER_JULIA = 512


def julia_fa_ung(z0: complex, c: complex, max_iter: int = _MAX_ITER_JULIA) -> float:
    """JuliaFaUng — smooth Julia-set iteration count for starting point z₀.

    Uses the standard smooth escape-time algorithm:
        smooth_i = i + 1 - log₂(log₂(|z|))
    Returns float(max_iter) for bounded (interior) points.
    """
    z = z0
    for i in range(max_iter):
        if abs(z) > 2.0:
            log_zn = math.log(abs(z))
            nu     = math.log(log_zn / math.log(2)) / math.log(2)
            return float(i) + 1.0 - nu
        z = z * z + c
    return float(max_iter)


# ── JuliaFaFy — full Julia grid accumulation ─────────────────────────────────
# Kobra: [MavoJuliaFaFy MavoJuliaFaUng MavoOs MavoIer MavoShiKaKoKiEl Kyra Kysha ...]
# Maps to: _mandelbrot_smooth grid loop in ambroflow.ko.render

def julia_fa_fy(
    c:        complex,
    size:     int   = 512,
    half:     float = 1.8,
    max_iter: int   = _MAX_ITER_JULIA,
) -> list[list[float]]:
    """JuliaFaFy — accumulate full Julia-set grid of smooth iteration counts.

    The grid covers [-half, half] × [-half, half] in the complex plane.
    Returns a size×size list of smooth iteration counts.
    """
    x_min, x_max = -half, half
    y_min, y_max = -half, half
    grid: list[list[float]] = []
    for row in range(size):
        im   = y_min + (y_max - y_min) * row / size
        line: list[float] = []
        for col in range(size):
            re = x_min + (x_max - x_min) * col / size
            line.append(julia_fa_ung(complex(re, im), c, max_iter))
        grid.append(line)
    return grid


