# ── PufFyLoVaShy — BoK state variable class ──────────────────────────────────
# Kobra: [MavoPufFyLoVaShy MavoAzothLo MavoMobiusCoil MavoCoilEp ... MavoMavo]

class PufFyLoVaShy:
    """PufFyLoVaShy — BreathOfKo state variable (compiled Kobra form).

    Holds the full Azoth+coil+density state.  Method names map to
    their ambroflow.ko.breath.BreathOfKo equivalents for test compatibility.
    """

    def __init__(
        self,
        layer_densities: dict[int, float] | None = None,
        coil_position:   float = 6.0,
        flag_weight:     float = 0.0,
        games_played:    int   = 0,
    ) -> None:
        self.layer_densities: dict[int, float] = (
            layer_densities if layer_densities is not None
            else {i: 0.5 for i in range(1, 25)}
        )
        self.coil_position = coil_position
        self.flag_weight   = flag_weight
        self.games_played  = games_played

    # Interface compatibility with BreathOfKo

    def azoth(self) -> complex:
        """Azoth complex number — matches BreathOfKo.azoth()."""
        return azoth_lo(self.layer_densities, self.coil_position, self.flag_weight)

    def gaoh_constant(self) -> complex:
        """Gaoh constant — matches BreathOfKo.gaoh_constant()."""
        return mobius_coil(self.coil_position)

    def boundedness(self) -> str:
        """Boundedness string — matches BreathOfKo.boundedness()."""
        it = azoth_su_foa(self.layer_densities, self.coil_position, self.flag_weight)
        if it >= _MAX_ITER_JULIA:
            return "bounded"
        elif it >= _MAX_ITER_JULIA // 2:
            return "edge"
        return "unbounded"

    def ko_path(self) -> str:
        """Ko's path selection: Kaganue or Drovitth."""
        it = azoth_su_foa(self.layer_densities, self.coil_position, self.flag_weight)
        return ko_foa_shi_ke_wu_ung(it)

    def snapshot(self) -> dict:
        """Snapshot dict — interface-compatible with BreathOfKo.snapshot()."""
        az = self.azoth()
        return {
            "layer_densities": dict(self.layer_densities),
            "coil_position":   self.coil_position,
            "games_played":    self.games_played,
            "azoth":           [az.real, az.imag],
            "boundedness":     self.boundedness(),
            "ko_path":         self.ko_path(),
        }

    def __repr__(self) -> str:
        return (
            f"PufFyLoVaShy(coil={self.coil_position:.3f}, "
            f"games={self.games_played}, "
            f"boundedness={self.boundedness()!r})"
        )


# ── PufFyLoShak — BoK snapshot update ────────────────────────────────────────
# Kobra: [MavoPufFyLoShak MavoPufFyLoVaShy MavoGaohNaAERuKi MavoWu ... MavoGwev MavoSava]

def puf_fy_lo_shak(
    state:               "PufFyLoVaShy",
    calibration_density: float,
) -> "PufFyLoVaShy":
    """PufFyLoShak — integrate one calibration pass into the BoK state.

    Updates all 24 layer densities as a rolling average, then advances
    the coil position by one game increment.
    """
    n             = state.games_played + 1
    new_densities = {}
    for idx, current in state.layer_densities.items():
        new_densities[idx] = round((current * (n - 1) + calibration_density) / n, 4)
    return PufFyLoVaShy(
        layer_densities = new_densities,
        coil_position   = coil_ep(state.coil_position),
        flag_weight     = state.flag_weight,
        games_played    = n,
    )


