# ── AzothMobiusFoa — Möbius-folded Azoth ─────────────────────────────────────
# Kobra: [MavoAzothMobiusFoa MavoAzothShak MavoGaohNaAERuKi MavoHa MavoSy ...]

def azoth_mobius_foa(
    layer_densities: dict[int, float],
    coil_position:   float,
    flag_weight:     float = 0.0,
) -> complex:
    """AzothMobiusFoa — Möbius-folded Azoth: rotate by coil's imaginary phase."""
    base  = azoth_shak(layer_densities, coil_position, flag_weight)
    c     = mobius_coil(coil_position)
    theta = math.atan2(base.imag, base.real)
    r     = abs(base)
    fold  = complex(
        r * math.cos(theta + c.imag),
        r * math.sin(theta + c.imag),
    )
    return fold


