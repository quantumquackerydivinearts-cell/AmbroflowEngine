WRAITH_BY_ID: dict[str, VoidWraith] = {w.id: w for w in VOID_WRAITHS}
WRAITH_BY_NAME: dict[str, VoidWraith] = {w.name: w for w in VOID_WRAITHS}


# ── Observer — tracks per-player opportunity counts and fires observations ────

class WraithObserver:
    """
    Tracks opportunity counts and fires Void Wraith observations via OrreryClient.

    One WraithObserver per player per session.
    Opportunity tracking:
      - Each time an action is available, call `opportunity(action_key)`.
      - Each time the player takes it, call `taken(action_key)`.
      - When the pattern threshold is crossed, an observation fires.

    Parameters
    ----------
    actor_id:
        Player identifier.
    orrery:
        OrreryClient — observations are fired here.
    """

    def __init__(self, actor_id: str, orrery: Any) -> None:
        self._actor_id   = actor_id
        self._orrery     = orrery
        self._opps:  dict[str, int] = {}
        self._taken: dict[str, int] = {}
        self._fired: set[str]       = set()   # observation keys already fired this session

    def opportunity(self, action_key: str) -> None:
        self._opps[action_key] = self._opps.get(action_key, 0) + 1

    def taken(self, action_key: str) -> None:
        self._taken[action_key] = self._taken.get(action_key, 0) + 1

    def life_made_absent(self, target_id: str, context: dict[str, Any] | None = None) -> None:
        """Negaya (Knower of Bodies) always observes a life made absent."""
        self._orrery.void_wraith_observe(ObservationKind.LIFE_MADE_ABSENT, {
            "actor_id": self._actor_id,
            "target_id": target_id,
            "wraith": "Negaya",
            **(context or {}),
        })

    def check_omission_pattern(self, action_key: str, kind: ObservationKind = ObservationKind.OMISSION_PATTERN) -> None:
        """
        Check whether the omission pattern for `action_key` crosses Vios's threshold.
        If so, fire an observation (once per session per action_key).
        """
        vios = WRAITH_BY_NAME["Vios"]
        opps  = self._opps.get(action_key, 0)
        taken = self._taken.get(action_key, 0)
        missed = opps - taken

        if opps < vios.min_repetitions:
            return
        if opps == 0:
            return
        ratio = missed / opps
        fire_key = f"vios.{action_key}"
        if ratio >= vios.threshold_ratio and fire_key not in self._fired:
            self._fired.add(fire_key)
            self._orrery.void_wraith_observe(kind, {
                "actor_id": self._actor_id,
                "action_key": action_key,
                "opportunities": opps,
                "taken": taken,
                "missed": missed,
                "ratio": round(ratio, 3),
                "wraith": "Vios",
            })

    def dream_entry_observed(self, entry_id: str, title: str) -> None:
        """Negaya observes every dream journal entry."""
        self._orrery.void_wraith_observe(ObservationKind.DREAM_ENTRY, {
            "actor_id": self._actor_id,
            "entry_id": entry_id,
            "title": title,
            "wraith": "Negaya",
        })

    def snapshot(self) -> dict[str, Any]:
        return {
            "actor_id":  self._actor_id,
            "opportunities": dict(self._opps),
            "taken":         dict(self._taken),
            "fired_this_session": list(self._fired),
        }
