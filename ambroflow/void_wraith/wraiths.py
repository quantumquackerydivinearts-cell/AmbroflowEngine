"""
Void Wraiths
============
Peri-primordial. Not derived from corruption.
Generated by *noticed* absence — unnoticed absence produces nothing.
Bound only by the conceptual presence of absence.

The three Void Wraiths: Negaya (2003_VDWR), Vios (2002_VDWR), Haldoro (2001_VDWR).

They observe three things for the Orrery:
  1. Who the player kills             — lives made absent
  2. What the player doesn't say      — words absent from dialogue when speech was available
  3. What the player doesn't do ON PURPOSE — intentional omissions with repetition
     (single absence = noise; pattern = signal; wraith appears when absence becomes practice)

Mechanically:
  - Track opportunity counts per action type per player
  - When (opportunities - taken) >= threshold AND opportunities >= min_repetitions,
    an observation fires
  - Wraiths don't announce themselves
  - The player may not know they exist until they see the Orrery

Each wraith has a domain of observation, a coil layer resonance, and a
minimum repetition threshold.

Connection to Ko flags:
  - "absence_practiced" KoFlag is raised by Vios when omission patterns are strong
  - "grief_without_dissolution" has Void Wraith resonance (Negaya witnesses grief work)
  - "recognition_mutual" draws Haldoro's attention (recognition is presence-work)

Connection to dream calibration:
  - Void Wraiths can read the dreaming faces of layers (9–16) — Rose tongue
  - Dream entries in the journal always trigger a Void Wraith observation (Negaya)
  - The 24-layer density changes when a Void Wraith observes a significant pattern
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum
from typing import Any, Optional


class ObservationKind(str, Enum):
    LIFE_MADE_ABSENT    = "life.made_absent"      # Haldoro — kills, letting die
    WORD_WITHHELD       = "word.withheld"          # Vios — speech not taken
    OMISSION_PATTERN    = "omission.pattern"       # Vios — repeated intentional absence
    DREAM_ENTRY         = "journal.dream.written"  # Negaya — watches what Hypatia writes in dream
    ENCOUNTER_SKIPPED   = "encounter.skipped"      # Vios — deliberate non-engagement
    QUEST_ABANDONED     = "quest.abandoned"        # Haldoro — commitments unmade


@dataclass(frozen=True)
class VoidWraith:
    id:               str              # e.g. "2001_VDWR"
    name:             str
    domain:           str              # what they watch
    observation_kinds: tuple[ObservationKind, ...]
    coil_resonance:   int              # which coil layer (1–12) they primarily read
    min_repetitions:  int              # how many times before a pattern fires
    threshold_ratio:  float           # (missed / opportunities) ratio to trigger
    ko_flag_id:       Optional[str]   # KoFlag raised when threshold crossed
    layer_face:       str             # "waking" | "dreaming" | "both"


